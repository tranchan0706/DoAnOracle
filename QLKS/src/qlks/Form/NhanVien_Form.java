    public boolean ktragioitinh() {
        if (jTextFieldGIOITINH.getText().toString().equals("NAM")
                || jTextFieldGIOITINH.getText().toString().equals("NU")) {
            return true;
        } else {
            JOptionPane.showMessageDialog(this, "Vui lòng nhập lại giới tính là NAM or NU!!", "Thong bao", 2);
            return false;
        }
    }
