    public ArrayList<NhanVien> layDanhSachNhanVien(String sql) {
        ArrayList<NhanVien> dsnv = new ArrayList<NhanVien>();
        Connection con = getConnection(Hostname, Port, SID, Username, Password);
        try {
            st = (Statement) con.createStatement();
            // Thưcj thi câu lệnh truy vấn
            ResultSet rs = st.executeQuery(sql);

            NhanVien nv;
            while (rs.next()) {
                nv = new NhanVien(rs.getString("MANV"), rs.getString("TENNV"), rs.getString("CHUCVU"),
                         rs.getInt("LUONGNV"), rs.getDate("NGAYSINH"), 
                        rs.getString("GIOITINH"), rs.getString("MANQL"), rs.getString("ANHNV"));

                //Thêm vào danh sách
                dsnv.add(nv);
            }
        } catch (Exception ex) {
            ex.printStackTrace();

        }
        return dsnv;
    }

    public void hienThiDanhSachNhanVien() {

        String colTieuDe1[] = new String[]{"Mã Nhân Viên", "Tên Nhân Viên", "Chức Vụ", "Lương", "Ngày Sinh", "Giới Tính", "Mã NQL", "Ảnh NV"};
        String sql = "select * from hr.nhanvien";
        ArrayList<NhanVien> dsnv = layDanhSachNhanVien(sql);
        DefaultTableModel model = new DefaultTableModel(colTieuDe1, 0);

        Object[] row;

        for (int i = 0; i < dsnv.size(); i++) {

            row = new Object[8];

            // GÁN GIÁ TRỊ
            row[0] = dsnv.get(i).getMANV();
            row[1] = dsnv.get(i).getTENNV();
            row[2] = dsnv.get(i).getCHUCVU();
            row[3] = dsnv.get(i).getLUONGNV();
            row[4] = dsnv.get(i).getNGAYSINH();
            row[5] = dsnv.get(i).getGIOITINH();
            row[6] = dsnv.get(i).getMANQL();
            row[7] = dsnv.get(i).getANHNV();

            model.addRow(row);
        }

        //    }catch(ArrayIndexOutOfBoundsException ex){
        jTableNhanvien.setModel(model);

    }

