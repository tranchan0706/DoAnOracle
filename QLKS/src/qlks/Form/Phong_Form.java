    public ArrayList<Phong> layDanhSachPhong(String sql) {
        ArrayList<Phong> dsp = new ArrayList<Phong>();
        Connection con = getConnection(Hostname,Port,SID,Username,Password);
        try {
            st = (Statement) con.createStatement();

            // Thưcj thi câu lệnh truy vấn
            ResultSet rs = st.executeQuery(sql);

            Phong p;
            while (rs.next()) {
                p = new Phong(rs.getString("SOPHONG"), rs.getString("LOAIPHONG"), rs.getInt("GIAPHONG"), rs.getString("TINHTRANG"));

                //Thêm vào danh sách
                dsp.add(p);
            }
        } catch (Exception ex) {
            ex.printStackTrace();

        }
        return dsp;
    }

    public void hienThiDanhSachPhong() {
        String colTieuDe1[] = new String[]{"Số Phòng", "Loại Phòng", "Giá Phòng", "Tình Trạng"};
        String sql = "SELECT * FROM hr.phong";
        ArrayList<Phong> dsp = layDanhSachPhong(sql);

        DefaultTableModel model = new DefaultTableModel(colTieuDe1, 0);

        Object[] row;

        for (int i = 0; i < dsp.size(); i++) {

            row = new Object[4];

            // GÁN GIÁ TRỊ
            row[0] = dsp.get(i).getSOPHONG();
            row[1] = dsp.get(i).getLOAIPHONG();
            row[2] = dsp.get(i).getGIAPHONG();
            row[3] = dsp.get(i).getTINHTRANG();

            model.addRow(row);
        }

        //}catch(ArrayIndexOutOfBoundsException ex){
        jTablePHONG.setModel(model);

    }

