    public static Boolean kiemtrathd(ArrayList<HoaDon> dshd,String sop) {
        for (HoaDon c : dshd) {
            if (c.getSOPHONG().equals(sop)) {
                return true;
            }
        }
        return false;
    }
    public Boolean kiemtrathuehd() {
        if (kiemtrathd(dshd, jTextFieldSOPHONG.getText().trim())) {
            JOptionPane.showMessageDialog(this, "Phòng này đang được Thuê, không xóa được!!", "Thong bao", 2);
            return true;
        }
        return false;
    }
