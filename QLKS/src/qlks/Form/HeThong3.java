    private void btn_hienthiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_hienthiActionPerformed
        // TODO add your handling code here:
        title.clear();
        data.clear();
        title.add("SID");
        title.add("SERIAL");
        title.add("USERNAME");
        Connection con = getConnection(Hostname,Port,SID,Username,Password);
        try {
            st = (Statement) con.createStatement();
            String sql = "select sid, serial#,username from v$session where username is not null";
            //String sql = "select sid, serial#,username from v$session where sid in(select blocking_session from v$session )";
            System.out.print(sql);
            // Thưc thi câu lệnh truy vấn
            ResultSet rs = st.executeQuery(sql);

            while (rs.next()) {
                Vector v = new Vector();
                v.add(rs.getString(1));
                v.add(rs.getString(2));
                v.add(rs.getString(3));
                data.add(v);
            }
        } catch (Exception ex) {
            ex.printStackTrace();

        }

        tbl_session.setModel(new DefaultTableModel(data, title));
    }//GEN-LAST:event_btn_hienthiActionPerformed

    private void btn_xoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_xoaActionPerformed
        // TODO add your handling code here:
        int pos = tbl_session.getSelectedRow();
        if (pos < 0) {
            JOptionPane.showMessageDialog(rootPane, "Vui Lòng Chọn Dòng Cần Xóa");
            return;
        }
        int yeucau = JOptionPane.showConfirmDialog(null, "Bạn có muốn xoá session này không?", null, JOptionPane.YES_NO_OPTION);
        if (yeucau == JOptionPane.YES_OPTION) {
            Connection con = getConnection(Hostname,Port,SID,Username,Password);
            try {
                st = (Statement) con.createStatement();
                String sql = "alter system kill session '" + txt_sid.getText() + "," + txt_SERIAL.getText() + "'";
                System.out.print(sql);
                // Thưc thi câu lệnh truy vấn
                ResultSet rs = st.executeQuery(sql);
                if (rs != null) {
                    System.out.println();
                    System.out.println("=> thành công");
                    JOptionPane.showMessageDialog(rootPane, "Xóa thành công!");
                    data.remove(pos);
                    tbl_session.setModel(new DefaultTableModel(data, title));
                }
            } catch (Exception ex) {
                System.out.println();
                System.out.println("=> không thành công");
                JOptionPane.showMessageDialog(rootPane, "Xóa Thất Bại! Session đang đăng nhập !");
                ex.printStackTrace();
            }
        }
    }//GEN-LAST:event_btn_xoaActionPerformed

