    public void excCommand(String t) {
        try {

            ProcessBuilder builder = new ProcessBuilder("cmd.exe", "/c", t);
            // thực thi command line
            System.out.print(t);
            Process p = builder.start();
            // lấy kết quả trả về trên command line
            BufferedReader r = new BufferedReader(new InputStreamReader(p.getInputStream()));
            String line;
            while (true) {
                line = r.readLine();
                if (line == null) {
                    break;
                }
                System.out.println(line);
                
            }
            JOptionPane.showMessageDialog(rootPane,t);
        } catch (IOException ex) {
            System.out.println("loi !!" + ex.toString());
            JOptionPane.showMessageDialog(rootPane, "Lỗi!"+"\t\n"+t);
        }
    }

    public Boolean kiemtratxt() {
        if (txt_user.getText().equals("") || txt_directory.getText().equals("") || txt_dumpfile.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Vui lòng nhập đầy đủ thông tin!", "Thong bao", 2);
            return false;
        }
        return true;
    }
